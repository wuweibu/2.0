# 示例 1: 简单的浮点数加法
a = 0.1
b = 0.2
c = a + b
d = 0.3
result = a + b

print("0.1 + 0.2 =", result)
print("0.1 + 0.2 = 0.3:", c == d)
# 你可能希望结果是 0.3，但实际输出会稍微偏差

# 示例 2: 浮点数的精度问题（或许已经得到修正）
x = 1.1 * 2
y = 2.2


print("1.1 * 2 =", x)
print("2.2 =", y)
print("1.1 * 2 == 2.2:", x == y)  # 比较两个浮点数是否相等
